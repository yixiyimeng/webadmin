<template>
  <component :is="component" :data="data" v-if="component" />
</template>
<script>
  export default {
    name: 'temp',
    props: ['data', 'type'],
    data() {
      return {
        component: null,
      }
    },
    computed: {
      loader() {
        if(!this.type) {
          return null
        }
        return() =>
         import(`@/${this.type}`)
      }
    },
    mounted() {
      var that = this;
      this.loader()
        .then(() => {
          this.component = () => this.loader();
			  })
        .catch(() => {
          // this.component = () => import('templates/default')
					console.log(this.type)
        })
    },
  }
</script>